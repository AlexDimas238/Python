#For Loop com Numeros
# Imprimir de 1 a 5
# RANGE é a qtd de loops

for numero in range(5):
  print(numero)
print('=======PRÓXIMO========')

for numero in range(1, 6):  
  print(numero) 
print('=======PRÓXIMO========')

#Com STEP (Saltos)
for numero in range(0,20, 3):
  print(numero)
print('=======PRÓXIMO========')